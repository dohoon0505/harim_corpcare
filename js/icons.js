/** Inject the sprite once (called from main.js). */
export function buildSprite() {
  if (document.getElementById("icon-sprite")) return;
  const symbols = Object.entries(P)
    .map(
      ([name, inner]) =>
        `<symbol id="i-${name}" viewBox="0 0 24 24">${inner}</symbol>`
    )
    .join("");
  const svg = document.createElementNS("http://www.w3.org/2000/svg", "svg");
  svg.id = "icon-sprite";
  svg.setAttribute("aria-hidden", "true");
  svg.innerHTML = symbols;
  document.body.appendChild(svg);
}

/** icon('bell', {size:18, cls:'text-blue'}) → trusted <svg> markup. */
export function icon(name, { size = 20, cls = "" } = {}) {
  const id = name.replace(/[A-Z]/g, (m) => "-" + m.toLowerCase());
  return raw(
    `<svg class="icon ${cls}" width="${size}" height="${size}" aria-hidden="true" focusable="false"><use href="#i-${id}"/></svg>`
  );
}
